# DDL
# DDL is short name of Data Definition Language, which deals with database schemas and descriptions
# of how the data should reside in the database.

# CREATE – to create database and its objects like (table, index, views, store procedure, function and triggers).
# ALTER – alters the structure of the existing database.
# DROP – delete objects from the database.
# TRUNCATE – remove all records from a table; also, all spaces allocated for the records are removed.
# COMMENT – add comments to the data dictionary.
# RENAME – rename an object.

# DML
# DML is short name of Data Manipulation Language which deals with data manipulation, and
# includes most common SQL statements such SELECT, INSERT, UPDATE, DELETE etc, and
# it is used to store, modify, retrieve, delete and update data in database.

# SELECT – retrieve data from one or more tables.                                                                                                                                                                                                                                    DELETE – delete all records from a table.
# MERGE – UPSERT operation (insert or update)
# CALL – call a PL/SQL or Java subprogram.
# EXPLAIN PLAN – interpretation of the data access path.
# LOCK TABLE – concurrency control.